public class resetBrd
{
    public static void resetBoard(char [][] gameBoard)
    {
            gameBoard[0][0] = '_';
            gameBoard[0][2] = '_';
            gameBoard[0][4] = '_';
            gameBoard[1][0] = '_';
            gameBoard[1][2] = '_';
            gameBoard[1][4] = '_';
            gameBoard[2][0] = '_';
            gameBoard[2][2] = '_';
            gameBoard[2][4] = '_';
    }
}